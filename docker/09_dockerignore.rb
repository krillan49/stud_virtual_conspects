# .dockerignore - файл который сообщает Docker, какие файлы и каталоги исключить из контекста сборки, чтобы избежать отправки нежелательных файлов и каталогов в сборщик. Например, если есть минимизированные ресурсы, файлы js, css, которые могут меняться при изменении исходного кода. Это также относится к файлам gitignore. Список рекомендуемых к игнорированию файлов можно нагуглить.

# При запуске команды сборки клиент сборки ищет файл с именем .dockerignore в корневом каталоге контекста. Если этот файл существует, файлы и каталоги, соответствующие шаблонам в файлах, удаляются из контекста сборки перед отправкой в ​​сборщик.

# Если используется несколько Dockerfile, можно использовать разные файлы игнорирования для каждого Dockerfile. Используются специальное соглашение об именовании для файлов игнорирования. Поместите файл игнорирования в тот же каталог, что и Dockerfile, и добавьте к файлу игнорирования префикс в виде имени конкретного Dockerfile. Например, для докерфаила с именем "build.Dockerfile" докеригнор фаил с именем "build.Dockerfile.dockerignore", а для докерфаила "lint.Dockerfile" докеригнор фаил "lint.Dockerfile.dockerignore". Файл игнорирования, специфичный для конкретного Dockerfile, имеет приоритет над .dockerignore файлом в корне контекста сборки, если оба они существуют.



