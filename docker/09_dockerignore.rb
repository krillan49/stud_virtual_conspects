# Файл .dockerignore это список шаблонов, разделенных новой строкой, аналогичных файловым глобсам оболочек Unix

# Комментарии в .dockerignore файле начинается с символа "#"
#/this/is/a/comment

# Начальные и конечные слеши игнорируются:
# /foo/bar/             - исключают фаил или директорию foo/bar от корня контекста сборки(?? Dockerfile)
# foo/bar               - исключают фаил или директорию foo/bar от корня контекста сборки(?? Dockerfile)

# */temp*	              - исключить файлы и каталоги, имена которых начинаются с "temp" в любом подкаталоге от корня. Например, "/somedir/temporary.txt" или "/somedir/temp"

# */*/temp*	            - исключить файлы и каталоги, начинающиеся с "temp" из любого подкаталога, который находится на два уровня ниже корня. Например, "/somedir/subdir/temporary.txt"

# temp?	                - исключить файлы и каталоги в корневом каталоге, имена которых имеют расширение из одного символа "temp". Например, "/tempa" и "/tempb"

# Docker поддерживает подстановочную строку "**", которая соответствует любому количеству каталогов (включая ноль каталогов)
# **/*.go               - исключает все файлы, которые заканчиваются на ".go", найденные где-либо в контексте сборки


# Строки с !(восклицательным знаком), позволяют делать исключения из исключений. Каждая последующая строка в .dockerignore более приоритетна и перебивает или частично перебивает противоположные по смыслу предыдущие

# Исключаем все md-фаилы в каталоге контекста, за исключением README.md исключены:
# *.md                - тут исключаем все md фаилы
# !README.md          - тут включаем(исключения из исключений) фаил README.md

# Исключены все md-файлы, кроме файлов, где начало имени "README", но "README-secret.md" исключен
# *.md                - тут исключаем все md фаилы
# !README*.md         - тут включаем(исключения из исключений) фаилы README*.md
# README-secret.md    - тут исключаем фаил README-secret.md

# Исключены все md-файлы, кроме файлов, где начало имени "README". Средняя строка не имеет значения, поскольку !README*.md включает README-secret.md и идет после нее
# *.md                - тут исключаем все md фаилы
# README-secret.md    - тут исключаем фаил README-secret.md
# !README*.md         - тут включаем(исключения из исключений) фаилы README*.md, соответсвенно и фаил README-secret.md

















